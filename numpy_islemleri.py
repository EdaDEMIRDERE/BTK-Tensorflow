import numpy as np

# (0, 10, 2) => 0 2 4 6 8
print("arange: np.arange(0, 10) \n",
      np.arange(0, 10), "\n")

print("zeros: np.zeros((3, 3)\n",
      np.zeros((3, 3)), "\n")

print("ones: np.ones((3, 3)\n",
      np.ones((3, 3)), "\n")

print("linspace: np.linspace(0, 10, 3)\n",
      np.linspace(0, 10, 3), "\n")

print("eye: np.eye(3)\n",
      np.eye(3), "\n")

print("random: np.random.randn(4)\n",
      np.random.randn(4, 4), "\n")

print("random: np.random.randint(4)\n",
      np.random.randint(1, 10, 5), "\n")
